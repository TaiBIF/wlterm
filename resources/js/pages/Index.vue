                <p class="col-md-8">雪霸國家公園內劃設許多生態保護區與特別景觀區。其中最知名的為發現於大甲溪上游武陵地區的陸封型櫻花鉤吻鮭。牠是冰河時期的孑遺生物，屬於陸封型的寒帶鮭鱒魚類，但卻能存活於地處熱帶與亞熱帶之台灣，是演化學中生物地理學的重大發現。由於具有重要的學術價值，但魚群數量卻早已稀少到瀕臨絕種的程度，且分布範圍現今只侷限於七家灣溪、高山溪與桃山北溪(曾，2005)，因此政府於1984年七月依「文化資產保存法」第49條及施行細則第72條之規定，指定並公告櫻花鉤吻鮭為珍貴稀有動物，將其列為重要文化資產之一，又稱為「國寶魚」。其現存棲息地的七家灣溪流域，在民國1999年由行政院農委會，公告為野生動物保護區。因此七家灣溪可以稱為「櫻花鉤吻鮭生態系」。雖然這種明星化的稱謂，表面上看似和生物多樣性保育精神有些背離，但是若其保育有助於七家灣溪生態系的完整，則櫻花鉤吻鮭扮演所謂傘護種(Umbrella species)的角色，也有利於與社會大眾的溝通和關注，也是另一種保育策略。</p>
