                    在武陵地區三條主要溪流共設立5個長期固定測站(圖6)，包括有勝溪下游靠近遊憩區收費站之有勝溪測站（測站#9），高山溪一號壩之高山溪測站（測站#8），七家灣溪一號壩上觀魚臺附近之七家灣溪一號壩測站（測站#4），桃山西溪武陵吊橋上游之桃山西溪測站（測站#2）與櫻花鉤吻鮭繁殖場測站（測站#5）。測站#9屬於高度優養化之有勝溪，附生藻類繁生，可與測站#8之寡營養鹽之高山溪測站相對照；測站#2、#4、#5皆屬於七家灣溪，其中測站#2與測站#8類似，但測站#4與測站#5皆為經過農業區之果園與菜園，與測站#1之有勝溪類似，可以相對照。測站#5為七家灣溪與高山溪之匯流處，可用以檢視匯流效應。今年增加四個一般測站，包括測站#1之桃山北溪、測站#3之七家灣溪二號壩、測站#6之萬壽橋上游以及測站#7之迎賓橋下之匯流口，與前述之五個固定測站共九個測站，藉以探討不同環境因子對主要生物群集之影響。
                    本整合計畫的研究重點將放在七家灣溪一號霸與三號壩之間約4 km的溪段，從事生物棲地選擇之重點深入研究。因為此區棲地較為多樣，且七家灣溪1-2號壩間櫻花鉤吻鮭多，但2-3號壩間鮭魚少。另外將增加桃山西溪測站(測站#2)作為對照，因為測站#3有農業污染。
                    在硝酸鹽來源研究方面，以NO3-N平均濃度最低的高山溪(測站#8)及桃山西溪(測站#2)二站作為背景測站。司界蘭溪上游NO3-N濃度接近高山溪，因此在司界蘭溪擇取下游測站作為司界蘭溪之代表，以檢視是否有人類活動影響。在七家灣溪沿岸，則擇取二號壩(測站#3)、一號壩(測站#4)、新繁殖場(測站#5)分別代表七家灣溪上、中、下游之測站。另選取藻類繁生、高度優養化之有勝溪水測站（測站#9）作為與其他測站之比對。
