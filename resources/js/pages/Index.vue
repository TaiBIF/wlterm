                <p class="col-md-8">
                    本計畫主要研究地點為武陵地區溪流，包括七家灣溪、桃山西溪、桃山北溪、高山溪及有勝溪(圖4)，進行實地觀測、調查與探勘。同時針對司界蘭溪現況做初步採樣與評估。
