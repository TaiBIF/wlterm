            <p>
                武陵地區七家灣溪在雪霸國家公園成立前由農委會所主導之研究主要著重在櫻花鉤吻鮭(Wang, 1989；Tsao, 1995；曾及楊，2001)及與鮭魚生存有關的水生昆蟲(林等，1987)。國家公園成立後才開始擴大到溪流其他相關生物之自然資源研究調查工作，因此與櫻花鉤吻鮭有關之研究計畫至今亦已完成超過百項之多（雪霸國家公園，2000; 2003），但資料一直缺乏整合(林，2002)。而生態相關資料的整合與模式建構為雪霸國家公園保育研究的主要目標之一，此亟需推動長期生態研究，建立生態系模式，以瞭解武陵地區生態系的長期生態現象與過程。
                溪流或濱岸棲地（riparian habitat）的兩生類及爬蟲類均未成為主要的研究對象、或只有非量化的附帶性質資料(袁，1995；呂，2002）。兩生類及爬蟲類，食性及生活史多樣，在溪流生態系中，可能是魚類食物種類(水生昆蟲)的主要競爭者及七家灣集水區生態系中的能量傳遞者，或是水中及近水棲地的掠食者。對於這些動物瞭解的增加，不但是生物學上有意義的研究，也能對七家灣溪生態系統的運作，有更進一步的認識(林及謝，2002)。對這類生物的長期監測，亦可作為反應環境狀況的良好指標。
                武陵地區溪流生態系受水環境參數之影響甚巨。七家灣溪沿岸之農業行為與人為活動對水質有一定程度之影響。而營養鹽在水中之不同分佈型態，決定其對生物之毒性或被生物攝食之可能性。農田中的肥料常在大雨沖刷後流入河川，造成溪水中的營養鹽濃度上升，進而影響櫻花鉤吻鮭之主要棲息地的溪流。雪霸國家公園區內的集水區亦是台灣中部地區水資源的主要供應區。山坡地的超限利用及濫墾的現象會使水質受到污染，大甲溪的德基水庫因上游山坡的墾植蔬果，導致水土流失，致水庫的壽命減短。而過度的使用肥料，也使水庫的水質呈現優養化，使得水資源的供應及品質受到潛在的威脅。
                物理棲地的改變對於生物族群影響亦很明顯。雪霸國家公園曾於1999至2001年間陸續完成四座防砂壩之壩體改善工程，為瞭解改善工程完成後對生物之影響應持續追蹤監測。然而近兩年多場颱風洪水使武陵地區溪流河道產生相較以往較為激烈之變化，而對應之河川物理棲地組成亦有明顯變異。在美國聯邦跨部會河川復育工作團隊(FISRWG, 1998)所完成之「河川廊道復育」中指出，對河川生態系具有較重要互動關係之非生物因子包括流況、水溫、遮蔽、溶氧量、pH值及河床底質等項。河川物理棲地類型直接受河川底質之粒徑組成所影響，而河川底質則與河川流量、河床坡度及泥砂來源息息相關。
                颱風對一地區生物群聚、食性與死亡率的影響甚鉅(Wiley, 1994)。九十三年八月艾利颱風挾著豐沛雨量，重創七家灣溪生態環境，濱溪植被和附生藻受到嚴重破壞、溪床變寬且上升、深潭減少，對當地生態系的造成衝擊。九十四年全年豪雨不斷，從三月雪、七月強烈颱風海棠，八月中度颱風馬莎、輕度颱風珊瑚、強烈颱風泰利，九月強烈颱風龍王等接連多次的風災侵襲。這兩年連續的幾個颱風對七家灣溪物理棲地、水質與生物的組成和數量的改變和回復所需時間長短，值得深入探討。此一探討將有助於雪霸管理處對於大自然干擾的處理策略和教育解說。
                為瞭解環境變遷對武陵地區生態系之影響，本計畫將利用物理棲地與化學水質所建立之資料，透過空間統計或其他分析方法(如類神經系統)，描述環境棲地之時空演變過程，瞭解環境變化(包含自然及人為因素)對於生態系之互動關聯，因此有必要以整合計畫的方式長期監測，以便本群體計畫能夠提供雪霸國家公園在武陵地區生態系管理上之參考及依據。
