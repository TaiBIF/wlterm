            <h5 class="blog-post-title" id="project-target">貳、計畫目標</h5>
            <p>
                本計畫簡稱武陵長期生態模式研究(WLTERM)，研究目標將完全配合「台灣長期生態研究網」計畫所規劃的長期生態目標，包括：1. 利用生態模式為方法，瞭解櫻花鉤吻鮭與武陵地區溪流的長期生態過程；2. 闡明溪流生物與生物間，以及溪流與溪岸生物間的交互作用；3. 尋找控制櫻花鉤吻鮭與武陵地區溪流生態過程的驅動力；4. 鑑別溪水中硝酸鹽之來源及人為活動之影響；5. 了解生物於各種物理棲地間之分布與差異；6. 了解物理棲地與各種生物在九十四年颱風洪水衝擊下之改變與恢復情形；7. 建立武陵地區生態研究的科學資料庫；8. 針對武陵地區長期生態監測項目、保育措施與管理策略提供建議。這在國內溪流生態系研究中是首見的。希望藉此奠定我國生態學研究之基礎，以進一步提昇我國在國際學術研究之地位。
                因此武陵WLTERM具體之研究內容及擬解決問題為：
                1. 建立溪流環境改變(包含自然及人為因素)與物理棲地與化學水質變化趨勢以及各種生物之關係
                2.選定重點區域與河段，了解不同類別生物彼此間及水質水文物理棲地等環境因子間之互動關係。
                3.蒐集武陵流域的人為活動，包括遊客、農業、肥料、農藥等資料，及經營管理所採取的措施實施情況，分析自然與人為干擾對生態系的影響。鑑別各溪溪水中硝酸鹽之可能來源，完成武陵地區內各溪溪水之硝酸鹽與農作肥料間關係之評估。
                4.架構小尺度之溪流水域生態系食物網模型 (圖2)，了解水域生態系中的生物組成、結構和各種生物的食性關係與交互作用、模擬生物於時間與空間之動態變化，及其受水域環境改變之影響。
                5.探討中尺度之溪流水域與濱岸植群之水陸域生態系之交互作用 (圖3)，了解物質與能量如何經由生物與非生物途徑在水陸域生態系間傳輸與作用。
                6.選定指標長期監測主要溪流與溪岸生物，包括植群、藻類、有機碎屑、昆蟲、其它無脊椎動物、兩生類、魚類與鳥類等在共同測站的時空分布變化，比較七家灣溪上中下游間，七家灣溪、高山溪、有勝溪與司界蘭溪(圖4)，以及武陵地區生物群聚結構間之關係。
                7.建議未來長期生態監測項目與經營管理策略。
                因此，本計畫的工作重點項目除了長期生態監測水文、水質與生物群集外，同時了解物理棲地與各種生物在九十四年一連串颱風洪水衝擊下之改變與恢復情形，以及生物於各種物理棲地間之分布與差異。最終目的是期望所建構模式具有實際預測環境變遷與人為干擾之能力，提供作為武陵地區溪流生態系管理之決策參考。
